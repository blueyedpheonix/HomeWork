package Calculator;

public class Operations {

    public double kefel(double num1,double num2)
    {
        return num1 * num2;
    }

    public double plus(double num1,double num2)
    {
        return num1 + num2;
    }
    public double minus(double num1,double num2)
    {
        return num1 - num2;
    }
    public double hiluk(double num1,double num2)
    {
        return num1/num2;
    }

}
